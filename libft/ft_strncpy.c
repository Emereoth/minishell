#include "libft.h"

char	*ft_strncpy(char *dst, char *src, size_t n)
{
	char	*tmp;

	tmp = dst;
	while (n > 0 && *src != '\0')
	{
		*tmp++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*tmp++ = '\0';
		n--;
	}
	return (dst);
}
