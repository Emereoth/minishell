#include "libft.h"

char	*ft_strncat(char *s1, char *s2, int n)
{
	int	c1;
	int	c2;

	c1 = ft_strlen(s1);
	c2 = 0;
	while ((s2[c2] != '\0') && c2 < n)
	{
		s1[c1 + c2] = s2[c2];
		s1[c1 + c2 + 1] = '\0';
		c2++;
	}
	return (s1);
}
